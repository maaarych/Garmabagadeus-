def read_file(file_path):
    '''
    this function reads names and iq from file and outputs it as dict
    '''
    with open(file_path,'r', encoding='utf-8') as file:
        next(file)
        result = {}
        for line in file:
            name, iq = line.strip().split(', ')
            result[name] = int(iq)
        return result

def rescue_people(smarties, limit_iq):
    '''
    this function finds how aliens will steal human from earth
    it outputs number of trips and list of people they will steal.
    '''
    sorted_smarties = dict(sorted(smarties.items(), key=lambda x: (-x[1], x[0])))
    trips = []
    while sorted_smarties:
        current_iq = 0
        trip = []
        for name, iq in sorted_smarties.items():
            if current_iq + iq <= limit_iq:
                trip.append(name)
                current_iq += iq
        sorted_smarties = {name: iq for name, iq in sorted_smarties.items() if name not in trip}
        trips.append(trip)
    return len(trips), trips
