def read_file(file_path):
    '''
    this function reads names and iq from file and oytputs it as dict
    >>> import tempfile
    >>> with tempfile.NamedTemporaryFile(mode = 'w', delete = False, encoding ='utf-8') as tmpfile:
    ...     _ = tmpfile.write('Elon Musk,165\\nMark Zuckerberg,152')
    >>> read_file(tmpfile.name)
    {'Mark Zuckerberg': 152}
    '''
    with open(file_path,'r', encoding='utf-8') as file:
        names=file.readlines()
        names=names[1:]
        result={}
        for i in names:
            name,iq = i.strip().split(', ')
            result[name]=int(iq)
        return result
def rescue_people(smarties, limit_iq):
    '''
    this function finds how alians will steal human from earth
    it outputs number of trips and list ofpeople they will steal.
    >>> rescue_people({'Elon Musk': 165, 'Mark Zuckerberg': 152, 'Will Smith': 157},500)
    (1, [['Elon Musk', 'Will Smith', 'Mark Zuckerberg']])
    '''
    sorted_smarties = dict(sorted(smarties.items(),key=lambda x:(-x[1],x[0])))
    trips=[]
    while sorted_smarties:
        current_iq=0
        lst=[]
        for name,iq in sorted_smarties.items():
            if current_iq + iq <= limit_iq:
                lst.append(name)
                current_iq+=iq
        for i in lst:
            sorted_smarties.pop(i)
        trips.append(lst)
    trips_all=len(trips)
    result=(trips_all,trips)
    return result
